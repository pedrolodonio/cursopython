#Soma
print (n1,"+",n2,"=",(n1+n2))
#Subtração
print (n1,"-",n2,"=",(n1-n2))
#Multiplicação
print (n1,"*",n2,"=",(n1*n2))
#Divisão
print (n1,"/",n2,"=",(n1/n2))
